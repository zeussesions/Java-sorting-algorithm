import java.util.ArrayList;

public class Array {
    public ArrayList<Integer> generate(int length) {
        ArrayList<Integer> list = new ArrayList<>();
        for (int i = 1; i < length + 1; i++) {
            list.add(i);
        }
        return list;
    }

    public ArrayList<Integer> shuffle(ArrayList<Integer> list) {
        return shuffle(list);
    }
}
